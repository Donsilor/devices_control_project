/**
 * 显示源生loading
 * @function showLoading
 * @memberOf module:UI
 * @author ive.chen
 * @example
 * HdSmart.UI.showLoading();
 */
export function showLoading() {
    HdIot.UI.showLoading({
        data: ''
    });
}

/**
 * 隐藏源生loading
 * @function hideLoading
 * @memberOf module:UI
 * @example
 * HdSmart.UI.hideLoading();
 */

export function hideLoading() {
    HdIot.UI.hideLoading({
        data: ''
    });
}